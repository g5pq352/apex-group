   function horizonHandler(el) {
        let _x = $(el).outerWidth(true) - $(window).width()

        var _tl = gsap.timeline({
            paused: 0,
        }).to(el, {
            x: -_x,
            ease: 'none'
        })

        if (_x > 0) {
            ScrollTrigger.create({
                toggleActions: "play pause resume reverse",
                trigger: el,
                start: "top 0%",
                end: `+=${_x}`,
                pin: el,
                pinSpace: false,
                scrub: true,
                animation: _tl,
                // markers: true
                onUpdate: (self) => {
                    let $contact = $("#horizontalWrap")
                    let m = $(window).width() / 2.3
                    $(".item-pic", $contact).each(function(i, el) {
                        let x = $(el).offset().left
                        if (x - m <= 0) {
                            $(el).removeClass("opacity-30").siblings(".item-pic").addClass("opacity-30")
                            $(el).addClass("z-10").siblings(".item-pic").removeClass("z-10")
                        }
                    })
                },
            });
        }
    }


